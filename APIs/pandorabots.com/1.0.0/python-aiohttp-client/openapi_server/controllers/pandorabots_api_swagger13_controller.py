async def atalk_bot(request: web.Request, app_id, botname, input, client_name=None, sessionid=None, recent=None) -> web.Response:
    """Anonymous Talk

    Start a conversation with the bot using the Anonymous Talk API. This method will allow you to request creation of an end-user client_name that can maintain persistent predicates per end-user talking to your bot. If client_name is NOT sent in the request, then Pandorabots will create a end-user client_name and return it in the response. Similar to the Talk to Bot API, Pandorabots will also return a new session ID if not included in the call. Use the session ID returned to group interactions together. &lt;p&gt;In addition to bot response and session ID, the HTTP response will include a new end-user client_name in the following format:&lt;br&gt;aiaas-XXX-user-nnnn, where XXX is your app_ID and nnnn is numeric starting with 0000 and incrementing after each request.&lt;br/&gt; &lt;/p&gt;&lt;p&gt;Malformed requests such as exceeding size of input or unknown end-user client_name returns 400 error code. Error code 412 is returned if the bot is not compiled or does not exist. Error code 429 is returned if your application has reached maximum plan API call limit.&lt;/p&gt;&lt;p&gt;&lt;pre class&#x3D;&#39;prettyprint&#39;&gt;curl -v  -X POST &#39;https://aiaas.pandorabots.com/atalk/APP_ID/BOTNAME?user_key&#x3D;USER_KEY&amp;input&#x3D;INPUT&#39;&lt;/pre&gt;&lt;/p&gt;

    :param app_id: Your Application ID
    :type app_id: str
    :param botname: The name of the bot. Format required is 3-64 characters in length and only numbers or lower-case letters [0-9][a-z]
    :type botname: str
    :param input: Message to be sent to the bot. This can contain multiple sentences. Currently the limit is 500 characters.
    :type input: str
    :param client_name: Leave blank to request Pandorabots to create a client_name which will support persistent predicates. Including a valid client_name in this parameter will work in the same way that Talk to Bot API but with persistent predicates. It is recommended to use this API only to create an end-user client_name, and then use normal Talk to Bot API to continue conversation with the bot.
    :type client_name: str
    :param sessionid: Session ID generated by Pandorabots. This allows the application to group individual conversations into a collection as needed. If not included in the call, Pandorabots will issue a new session ID. (4-byte integer type)
    :type sessionid: str
    :param recent: If true, the system will not signal an error if the bot is uncompiled, and will instead look for a previous version of the bot that is available.
    :type recent: str

    """
    return web.Response(status=200)


