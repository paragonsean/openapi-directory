void OAISnippetApi::updateSiteSnippet(const QString &site_id, const QString &snippet_id, const OAISnippet &snippet) {
    QString fullPath = QString(_serverConfigs["updateSiteSnippet"][_serverIndices.value("updateSiteSnippet")].URL()+"/sites/{site_id}/snippets/{snippet_id}");
    
    
    {
        QString site_idPathParam("{");
        site_idPathParam.append("site_id").append("}");
        QString pathPrefix, pathSuffix, pathDelimiter;
        QString pathStyle = "";
        if (pathStyle == "")
            pathStyle = "simple";
        pathPrefix = getParamStylePrefix(pathStyle);
        pathSuffix = getParamStyleSuffix(pathStyle);
        pathDelimiter = getParamStyleDelimiter(pathStyle, "site_id", false);
        QString paramString = (pathStyle == "matrix") ? pathPrefix+"site_id"+pathSuffix : pathPrefix;
        fullPath.replace(site_idPathParam, paramString+QUrl::toPercentEncoding(::OpenAPI::toStringValue(site_id)));
    }
    
    {
        QString snippet_idPathParam("{");
        snippet_idPathParam.append("snippet_id").append("}");
        QString pathPrefix, pathSuffix, pathDelimiter;
        QString pathStyle = "";
        if (pathStyle == "")
            pathStyle = "simple";
        pathPrefix = getParamStylePrefix(pathStyle);
        pathSuffix = getParamStyleSuffix(pathStyle);
        pathDelimiter = getParamStyleDelimiter(pathStyle, "snippet_id", false);
        QString paramString = (pathStyle == "matrix") ? pathPrefix+"snippet_id"+pathSuffix : pathPrefix;
        fullPath.replace(snippet_idPathParam, paramString+QUrl::toPercentEncoding(::OpenAPI::toStringValue(snippet_id)));
    }
    OAIHttpRequestWorker *worker = new OAIHttpRequestWorker(this, _manager);
    worker->setTimeOut(_timeOut);
    worker->setWorkingDirectory(_workingDirectory);
    OAIHttpRequestInput input(fullPath, "PUT");

    {

        
        QByteArray output = snippet.asJson().toUtf8();
        input.request_body.append(output);
    }
    for (auto keyValueIt = _defaultHeaders.keyValueBegin(); keyValueIt != _defaultHeaders.keyValueEnd(); keyValueIt++) {
        input.headers.insert(keyValueIt->first, keyValueIt->second);
    }


    connect(worker, &OAIHttpRequestWorker::on_execution_finished, this, &OAISnippetApi::updateSiteSnippetCallback);
    connect(this, &OAISnippetApi::abortRequestsSignal, worker, &QObject::deleteLater);
    connect(worker, &QObject::destroyed, this, [this]() {
        if (findChildren<OAIHttpRequestWorker*>().count() == 0) {
            Q_EMIT allPendingRequestsCompleted();
        }
    });
    _OauthMethod = 1;
    _implicitFlow.link();
    _passwordFlow.unlink();
    _authFlow.unlink();
    _credentialFlow.unlink();
    QStringList scope;
    auto token = _implicitFlow.getToken(scope.join(" "));
    if(token.isValid())
        input.headers.insert("Authorization", "Bearer " + token.getToken());

    _latestWorker = new OAIHttpRequestWorker(this, _manager);
    _latestWorker->setTimeOut(_timeOut);
    _latestWorker->setWorkingDirectory(_workingDirectory);

    connect(_latestWorker, &OAIHttpRequestWorker::on_execution_finished, this, &OAISnippetApi::updateSiteSnippetCallback);
    connect(this, &OAISnippetApi::abortRequestsSignal, _latestWorker, &QObject::deleteLater);
    connect(_latestWorker, &QObject::destroyed, [this](){
        if(findChildren<OAIHttpRequestWorker*>().count() == 0){
            Q_EMIT allPendingRequestsCompleted();
        }
    });

    _latestInput = input;
    _latestScope = scope;



    worker->execute(&input);
}

